void PrintLine(unsigned char line)
{
    for (int i = 0; i < 7; ++i)
    {
        putchar(line & 1 ? '*' : ' ');
        line >>= 1;
    }
    putchar('\n');
}

void PrintLetter(unsigned char letter)
{
    unsigned long long letterCode = ASCII_TABLE[letter];
    //print A with asterics
    for (int i = 0; i < 8; ++i)
    {
        PrintLine((unsigned char)(letterCode & 0xFF));
        letterCode >>= 8;
    }
}
